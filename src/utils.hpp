std::vector<float> getXCoordinates(int numPoints) {
    std::vector<float> x(numPoints);
    for (int i = 0; i < numPoints; i++) {
        float t = 2 * M_PI * i / (numPoints - 1);
        x[i] = 16 * std::pow(std::sin(t), 3);
    }
    return x;
}

std::vector<float> getYCoordinates(int numPoints) {
    std::vector<float> y(numPoints);
    for (int i = 0; i < numPoints; i++) {
        float t = 2 * M_PI * i / (numPoints - 1);
        y[i] = 13 * std::cos(t) - 5 * std::cos(2 * t) - 2 * std::cos(3 * t) - std::cos(4 * t);
    }
    return y;
}



